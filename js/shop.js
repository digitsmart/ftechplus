function myFunction() {
var x = document.getElementById("myDIV");
var y = document.getElementById("imgs");
if (x.style.display === "none") {
  x.style.display = "block";
  y.style.display = "none";
} else {
  x.style.display = "block";
  y.style.display = "none";
}
}

