function ouvrirPage() {
    	var a = document.getElementById("search").value;
    	
    	if (a === "inscription") {
    	window.open("Inscription.html");
    	}
    	
    	if (a === "chien") {
    	window.open("/customizedalert.html");
    	}
    	
    	if (a === "youtube") {
    	window.open("https://www.youtube.com/");
    	}
    	
    	
    	}